def _warn_if_runtime_outdated(
    blob_service_client,
    container: str,
    packages_path: str,
) -> None:
    """Emit a warning if the deployed runtime wheel is older than the CLI."""
    try:
        from packaging.version import Version
    except ImportError:
        return

    try:
        container_client = blob_service_client.get_container_client(container)
        prefix = f"{packages_path}/" if packages_path else "packages/"
        blobs = list(container_client.list_blobs(name_starts_with=prefix))
    except Exception:
        return

    deployed_version = None
    for blob in blobs:
        name = blob.name.split("/")[-1]
        if name.startswith("spark_kindling-") and name.endswith(".whl"):
            parts = name.split("-")
            if len(parts) >= 2:
                try:
                    v = Version(parts[1])
                    if deployed_version is None or v > deployed_version:
                        deployed_version = v
                except Exception:
                    pass

    if deployed_version is None:
        return

    try:
        cli_version = Version(_get_version("spark-kindling-cli"))
    except Exception:
        return

    if deployed_version < cli_version:
        click.echo(
            f"Warning: deployed runtime is v{deployed_version} but CLI is v{cli_version}. "
            "Run 'kindling workspace deploy' to update the runtime wheel.",
            err=True,
        )


def _upload_blob(
    blob_service_client,
    container: str,
    blob_path: str,
    data: bytes,
    overwrite: bool = True,
) -> bool:
    """Upload a blob. Returns True if uploaded, False if skipped."""
    blob_client = blob_service_client.get_blob_client(container=container, blob=blob_path)
    if not overwrite:
        try:
            blob_client.get_blob_properties()
            return False  # exists, skip
        except Exception:
            pass  # doesn't exist, proceed
    blob_client.upload_blob(data, overwrite=True)
    return True


def _find_wheels(dist_dir: Path, platform: Optional[str] = None) -> List[Path]:
    """Find runtime wheels, preferring the combined spark-kindling artifact."""
    combined_wheels = sorted(dist_dir.glob("spark_kindling-*.whl"))
    if combined_wheels:
        return combined_wheels

    if platform:
        return sorted(dist_dir.glob(f"kindling_{platform}-*.whl"))

    legacy_wheels: List[Path] = []
    for legacy_platform in ("synapse", "databricks", "fabric"):
        legacy_wheels.extend(dist_dir.glob(f"kindling_{legacy_platform}-*.whl"))
    return sorted(legacy_wheels)


def _deploy_wheels(
    blob_service_client,
    container: str,
    packages_path: str,
    wheels: List[Path],
) -> int:
    """Upload wheel files. Always overwrites (wheels are versioned)."""
    count = 0
    for wheel in wheels:
        dest = f"{packages_path}/{wheel.name}" if packages_path else wheel.name
        click.echo(f"  {wheel.name}")
        _upload_blob(blob_service_client, container, dest, wheel.read_bytes(), overwrite=True)
        count += 1
    return count


def _load_package_metadata(package_root: Path) -> Tuple[str, str]:
    """Read package name and version from a Poetry pyproject.toml."""
    pyproject_path = package_root / "pyproject.toml"
    if not pyproject_path.exists():
        raise click.ClickException(f"Package pyproject.toml not found: {pyproject_path}")

    try:
        try:
            import tomllib
        except ImportError:
            import tomli as tomllib  # type: ignore[no-redef]
    except ImportError as exc:
        raise click.ClickException(
            "Reading pyproject.toml on Python 3.10 requires tomli. "
            "Install tomli or run with Python 3.11+."
        ) from exc

    try:
        data = tomllib.loads(pyproject_path.read_text(encoding="utf-8"))
        poetry_data = data.get("tool", {}).get("poetry", {})
        package_name = str(poetry_data["name"]).strip()
        package_version = str(poetry_data["version"]).strip()
    except Exception as exc:
        raise click.ClickException(
            f"Could not read tool.poetry.name/version from {pyproject_path}: {exc}"
        ) from exc

    if not package_name or not package_version:
        raise click.ClickException(f"{pyproject_path} must define tool.poetry.name and version.")
    return package_name, package_version


def _wheel_distribution_name(package_name: str) -> str:
    """Normalize a Python distribution name the way wheel filenames do."""
    import re

    return re.sub(r"[-_.]+", "_", package_name).strip("_")


def _build_package_wheel(package_root: Path, dist_dir: Path) -> Path:
    """Build a package wheel with Poetry and return the matching wheel path."""
    package_name, package_version = _load_package_metadata(package_root)
    normalized_name = _wheel_distribution_name(package_name)

    resolved_dist = dist_dir.expanduser()
    if not resolved_dist.is_absolute():
        resolved_dist = package_root / resolved_dist
    resolved_dist.mkdir(parents=True, exist_ok=True)

    before = {path.resolve() for path in resolved_dist.glob("*.whl")}
    cmd = [
        "poetry",
        "build",
        "--format",
        "wheel",
        "--output",
        str(resolved_dist),
    ]
    click.echo(f"Building wheel in {package_root}...")
    result = subprocess.run(cmd, cwd=package_root, capture_output=True, text=True)
    if result.returncode != 0:
        stderr = (result.stderr or result.stdout or "").strip()
        raise click.ClickException(f"Package wheel build failed.\n{stderr}")

    matching = sorted(
        resolved_dist.glob(f"{normalized_name}-{package_version}-*.whl"),
        key=lambda path: path.stat().st_mtime,
        reverse=True,
    )
    if matching:
        return matching[0]

    created = sorted(
        [path for path in resolved_dist.glob("*.whl") if path.resolve() not in before],
        key=lambda path: path.stat().st_mtime,
        reverse=True,
    )
    if created:
        return created[0]

    raise click.ClickException(
        f"Build completed but no wheel was found in {resolved_dist} for "
        f"{package_name} {package_version}."
    )


