def _coerce_value(raw: str) -> Any:
    """Convert a CLI string value to its natural YAML type.

    Boolean strings (true/false/yes/no), null/none, integers, and floats are
    converted to their native Python types.  Wrap the value in quotes to force
    it to remain a string, e.g. ``'"true"'``.
    """
    if len(raw) >= 2 and raw[0] == raw[-1] and raw[0] in ("'", '"'):
        return raw[1:-1]

    lowered = raw.strip().lower()
    if lowered in ("true", "yes", "on"):
        return True
    if lowered in ("false", "no", "off"):
        return False
    if lowered in ("null", "none", "~"):
        return None

    try:
        return int(raw)
    except ValueError:
        pass
    try:
        return float(raw)
    except ValueError:
        pass
    return raw


def _discover_python_package_roots(pythonpath_entry: Path) -> List[str]:
    """Return importable package roots found under a PYTHONPATH entry.

    Includes both regular packages (__init__.py) and namespace packages (no
    __init__.py but still a valid importable directory on the path).
    """
    package_roots = []
    for candidate in sorted(pythonpath_entry.iterdir()):
        if candidate.is_dir() and not candidate.name.startswith((".", "_")):
            package_roots.append(candidate.name)
    return package_roots


def _resolve_local_package_path(package_path: Path) -> Tuple[Path, List[str]]:
    """Resolve a local package root/source directory and discover package roots."""
    resolved = package_path.expanduser().resolve()
    if not resolved.exists():
        raise click.ClickException(f"Local package path does not exist: {resolved}")
    if not resolved.is_dir():
        raise click.ClickException(f"Local package path must be a directory: {resolved}")

    src_dir = resolved / "src"
    if src_dir.is_dir():
        return src_dir, _discover_python_package_roots(src_dir)

    if (resolved / "__init__.py").is_file():
        return resolved.parent, [resolved.name]

    return resolved, _discover_python_package_roots(resolved)


def _set_nested_key(data: Dict[str, Any], dotted_key: str, value: Any) -> None:
    """Set a value in a nested dict using a dot-notation key.

    Intermediate dicts are created as needed.  Non-dict intermediates are
    overwritten so the requested path can be established.
    """
    keys = dotted_key.split(".")
    current = data
    for part in keys[:-1]:
        if part not in current or not isinstance(current[part], dict):
            current[part] = {}
        current = current[part]
    current[keys[-1]] = value


def _resolve_config_path(
    config_dir: Path,
    level: str,
    platform: Optional[str],
    environment: Optional[str],
    app_name: Optional[str],
) -> Path:
    """Return the target YAML config file for the given scope and level."""
    base_dir = config_dir / "data-apps" / app_name if app_name else config_dir

    if level == "platform":
        return base_dir / f"platform_{platform}.yaml"
    if level == "env":
        return base_dir / f"env_{environment}.yaml"
    return base_dir / "settings.yaml"


def _print_check_results(checks: List[Tuple[str, bool, str]]) -> bool:
    all_passed = True
    for name, passed, detail in checks:
        status = "PASS" if passed else "FAIL"
        click.echo(f"[{status}] {name}: {detail}")
        if not passed:
            all_passed = False
    return all_passed


def _discover_local_app_names() -> List[Tuple[str, Path]]:
    """Return (app_name, app_dir) for every app found under the current directory."""
    cwd = Path.cwd()
    candidate_dirs: List[Path] = []

    if (cwd / APP_CONFIG_FILE).exists():
        candidate_dirs.append(cwd)

    for pattern in ("apps", "src"):
        for d in sorted((cwd / pattern).glob("*")):
            if d.is_dir() and (d / APP_CONFIG_FILE).exists():
                candidate_dirs.append(d)

    results: List[Tuple[str, Path]] = []
    for app_dir in candidate_dirs:
        try:
            data = yaml.safe_load((app_dir / APP_CONFIG_FILE).read_text(encoding="utf-8")) or {}
            app_name = (data.get("name") or app_dir.name).strip()
        except Exception:
            app_name = app_dir.name
        if app_name:
            results.append((app_name, app_dir))
    return results


def _discover_app_py(app_path: Optional[Path]) -> Path:
    """Find a Kindling app.py from an explicit path or conventional local layout."""
    if app_path is not None:
        resolved_app_path = app_path.expanduser().resolve()
        if not resolved_app_path.exists():
            raise click.ClickException(f"app.py not found at: {resolved_app_path}")
        return resolved_app_path

    cwd = Path.cwd()
    candidates = [
        cwd / "app.py",
        *sorted((cwd / "apps").glob("*/app.py")),
        *sorted((cwd / "src").glob("*/app.py")),
        *sorted((cwd.parent / "src").glob("*/app.py")),
        # legacy repo-root layout: packages/<pkg>/src/<pkg>/app.py
        *sorted((cwd / "packages").glob("*/src/*/app.py")),
    ]
    found = [c.resolve() for c in candidates if c.exists()]
    if not found:
        raise click.ClickException(
            "Could not find app.py. Run from an app directory or repo root, "
            "or pass --app path/to/app.py"
        )
    if len(found) > 1:
        listed = "\n  ".join(str(p) for p in found)
        raise click.ClickException(
            f"Multiple app.py files found — pass --app to select one:\n  {listed}"
        )
    return found[0]


def _discover_app_py_under(app_root: Path) -> Path:
    """Discover app.py below an explicit app root."""
    root = app_root.expanduser().resolve()
    if root.is_file():
        return root

    candidates = [
        root / "app.py",
        *sorted((root / "apps").glob("*/app.py")),
        *sorted((root / "src").glob("*/app.py")),
        *sorted((root / "packages").glob("*/src/*/app.py")),
    ]
    found = [candidate.resolve() for candidate in candidates if candidate.exists()]
    if not found:
        raise click.ClickException(
            f"Could not find app.py under `{root}`. "
            "Pass a local app directory, repo root, or app.py path."
        )
    if len(found) > 1:
        listed = "\n  ".join(str(path) for path in found)
        raise click.ClickException(
            f"Multiple app.py files found under `{root}` - pass the app.py path to select one:\n  {listed}"
        )
    return found[0]


def _load_app_module(app_path: Path, env: Optional[str], config_dir: Optional[Path] = None) -> None:
    """Import app.py and call its initialize() function."""
    import importlib.util
    import inspect

    module_key = "_kindling_app"
    module_search_path = (
        app_path.parent.parent if app_path.parent.parent.name == "src" else app_path.parent
    )
    inserted_path = str(module_search_path)
    inserted = False
    if inserted_path not in sys.path:
        sys.path.insert(0, inserted_path)
        inserted = True

    try:
        spec = importlib.util.spec_from_file_location(module_key, app_path)
        if spec is None or spec.loader is None:
            raise click.ClickException(f"Could not load app.py at {app_path}")
        module = importlib.util.module_from_spec(spec)
        sys.modules[module_key] = module
        try:
            spec.loader.exec_module(module)
        except SyntaxError as exc:
            raise click.ClickException(
                f"Failed to import app.py: {exc}\n"
                f"  File: {app_path}\n"
                '  Hint: Check for syntax errors — run: python -c "import app" to see the full traceback.'
            ) from exc
        except ImportError as exc:
            raise click.ClickException(
                f"Failed to import app.py: {exc}\n"
                f"  File: {app_path}\n"
                '  Hint: Check for syntax errors — run: python -c "import app" to see the full traceback.'
            ) from exc

        initialize = getattr(module, "initialize", None)
        if initialize is None:
            raise click.ClickException(
                f"app.py at {app_path} has no initialize() function. "
                "Ensure it follows the Kindling app.py convention."
            )

        kwargs: Dict[str, Any] = {"env": env}
        if config_dir is not None:
            signature = inspect.signature(initialize)
            if "config_dir" not in signature.parameters:
                raise click.ClickException(
                    "The selected app.py initialize() function does not accept "
                    "--config. Update app.py or omit --config."
                )
            kwargs["config_dir"] = config_dir.expanduser().resolve()
        initialize(**kwargs)
    finally:
        sys.modules.pop(module_key, None)
        if inserted:
            try:
                sys.path.remove(inserted_path)
            except ValueError:
                pass


def _detect_platform_from_environment() -> Optional[str]:
    if os.getenv("FABRIC_WORKSPACE_ID"):
        return "fabric"
    if os.getenv("SYNAPSE_WORKSPACE_NAME"):
        return "synapse"
    if os.getenv("DATABRICKS_HOST"):
        return "databricks"
    return None


_PLATFORM_NOT_FOUND_MSG = (
    "Unable to determine platform. Set --platform or one of "
    "FABRIC_WORKSPACE_ID, SYNAPSE_WORKSPACE_NAME, DATABRICKS_HOST."
)


def _resolve_remote_platform(platform: Optional[str], require_credentials: bool = False) -> str:
    """Resolve remote platform and optionally enforce base credential variables."""
    resolved_platform = platform or _detect_platform_from_environment()
    if not resolved_platform:
        raise click.ClickException(_PLATFORM_NOT_FOUND_MSG)

    if require_credentials:
        missing = _missing_platform_vars(resolved_platform)
        if missing:
            raise click.ClickException(
                f"Missing required environment variables for {resolved_platform}: {', '.join(missing)}.\n"
                f"Run `kindling env check --platform {resolved_platform}` to verify your credentials."
            )

    return resolved_platform


def _validate_logs_options(
    from_line: int,
    size: int,
    stream: bool,
    poll_interval: float,
    max_wait: float,
) -> None:
    """Validate shared options for app/job log retrieval and streaming."""
    if from_line < 0:
        raise click.ClickException("--from-line must be greater than or equal to 0.")
    if size <= 0:
        raise click.ClickException("--size must be greater than 0.")
    if stream and poll_interval <= 0:
        raise click.ClickException("--poll-interval must be greater than 0.")
    if stream and max_wait <= 0:
        raise click.ClickException("--max-wait must be greater than 0.")


def _read_settings_app_name(settings_path: Path = Path("settings.yaml")) -> Optional[str]:
    """Read the app name from settings.yaml, returning None if not found or unreadable."""
    candidate = settings_path.expanduser()
    if not candidate.exists():
        return None
    try:
        data = yaml.safe_load(candidate.read_text(encoding="utf-8")) or {}
        if isinstance(data, dict):
            name = data.get("name")
            if name and str(name).strip():
                return str(name).strip()
    except (OSError, yaml.YAMLError) as exc:
        click.echo(f"Warning: could not parse {candidate}: {exc}", err=True)
    return None


