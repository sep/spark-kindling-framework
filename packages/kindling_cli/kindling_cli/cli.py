def _run_standalone_app(
    app_ref: str,
    env: Optional[str],
    config_dir: Optional[Path],
    quiet: bool,
    local_packages: Tuple[Path, ...],
    parameters_path: Optional[Path],
    param_overrides: Tuple[str, ...],
    no_logs: bool,
    no_wait: bool,
    json_output: bool,
    fail_on_error: bool = True,
    dotenv_paths: Tuple[Path, ...] = (),
) -> None:
    """Run an app locally by executing its entrypoint as a subprocess."""
    if parameters_path or param_overrides:
        raise click.ClickException(
            "Runtime parameters are not supported for standalone app runs yet."
        )
    if no_logs:
        raise click.ClickException("--no-logs is only valid for remote app runs.")
    if no_wait:
        raise click.ClickException("--no-wait is only valid for remote app runs.")

    source_path = Path(app_ref).expanduser()
    if not source_path.exists():
        raise click.ClickException(
            f"Standalone app runs require a local app path. `{app_ref}` was not found."
        )
    if source_path.is_file() and source_path.suffix.lower() == ".kda":
        raise click.ClickException(
            "Standalone app runs require an unpacked local app directory or "
            "app.py path, not a .kda package."
        )

    resolved_env = env or os.getenv("KINDLING_ENV", "local")
    resolved_app = _discover_app_py_under(source_path)
    app_dir = resolved_app.parent

    # Resolve config files: explicit --config dir overrides auto-discovery from app dir
    cfg_root = config_dir.expanduser().resolve() if config_dir else app_dir
    config_files = []
    base_cfg = cfg_root / "settings.yaml"
    if base_cfg.exists():
        config_files.append(str(base_cfg))
    env_cfg = cfg_root / f"settings.{resolved_env}.yaml"
    if env_cfg.exists():
        config_files.append(str(env_cfg))

    from kindling_cli.test_runner import load_dotenv as _load_dotenv

    for dotenv_path in dotenv_paths:
        _load_dotenv(dotenv_path)

    run_env = {**os.environ, "KINDLING_ENV": resolved_env}
    run_env.setdefault("KINDLING_SPARK_ENABLE_DELTA", "true")
    if local_packages:
        local_package_paths = []
        local_package_modules = []
        for path in local_packages:
            pythonpath_entry, package_modules = _resolve_local_package_path(path)
            local_package_paths.append(str(pythonpath_entry))
            local_package_modules.extend(package_modules)
        existing_pythonpath = run_env.get("PYTHONPATH")
        run_env["PYTHONPATH"] = os.pathsep.join(
            [*local_package_paths, *([existing_pythonpath] if existing_pythonpath else [])]
        )
        if local_package_modules:
            try:
                existing_modules = json.loads(run_env.get("KINDLING_LOCAL_PACKAGE_MODULES") or "[]")
            except json.JSONDecodeError:
                existing_modules = []
            if not isinstance(existing_modules, list):
                existing_modules = []
            run_env["KINDLING_LOCAL_PACKAGE_MODULES"] = json.dumps(
                [*local_package_modules, *existing_modules]
            )
    if quiet:
        run_env["KINDLING_LOG_LEVEL"] = "WARNING"

    runner_cmd = [
        sys.executable,
        "-m",
        "kindling_cli._runner",
        "--env",
        resolved_env,
        *[arg for cfg in config_files for arg in ("--config", cfg)],
        str(resolved_app),
    ]
    _emit_progress(f"Running app locally: {resolved_app}", json_output)
    result = subprocess.run(runner_cmd, env=run_env)

    succeeded = result.returncode == 0
    payload = {
        "app_path": str(resolved_app),
        "environment": resolved_env,
        "platform": "standalone",
        "succeeded": succeeded,
    }
    if not succeeded:
        _emit_result(payload, json_output, f"App run failed (exit code {result.returncode}).")
        if fail_on_error:
            raise click.exceptions.Exit(result.returncode)
        return

    _emit_result(payload, json_output, "App completed successfully.")


