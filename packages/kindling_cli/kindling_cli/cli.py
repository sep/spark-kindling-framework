def _print_platform_credential_check(platform: str) -> bool:
    """Print a human-readable credential check for the given platform.

    Each required env var is reported as SET or MISSING.  Missing vars include
    an ``export`` hint.  Returns ``True`` when all vars are set, ``False``
    when one or more are missing.
    """
    vars_for_platform = _PLATFORM_CREDENTIAL_VARS.get(platform, [])
    click.echo(f"Platform: {platform}")
    missing_count = 0
    col_width = max((len(v) for v in vars_for_platform), default=0) + 2
    for var in vars_for_platform:
        val = os.getenv(var)
        if val:
            click.echo(f"  {var:<{col_width}} SET")
        else:
            missing_count += 1
            hint = var.lower().replace("_", "-")
            click.echo(f"  {var:<{col_width}} MISSING  → set via: export {var}=<your-{hint}>")
    if missing_count:
        click.echo(
            f"\n{missing_count} missing."
            f" Run 'kindling env check --platform {platform}' again after setting them."
        )
    return missing_count == 0


def _missing_platform_vars(platform: str) -> List[str]:
    """Return required env vars for platform that are not currently set."""
    return [var for var in _PLATFORM_BASE_VARS.get(platform, []) if not os.getenv(var)]


def _check_java() -> Tuple[bool, str]:
    import subprocess

    try:
        result = subprocess.run(
            ["java", "-version"],
            capture_output=True,
            text=True,
            timeout=10,
        )
        # java -version writes to stderr
        output = (result.stderr or result.stdout).strip().split("\n")[0]
        return result.returncode == 0, output or "found"
    except FileNotFoundError:
        return False, "not found — install a JDK (>=11)"
    except Exception as exc:
        return False, f"error: {exc}"


_MODULE_TO_PACKAGE = {
    "delta": "delta-spark",
    "pyspark": "pyspark",
}


def _check_module(module: str) -> Tuple[bool, str]:
    import importlib.util

    spec = importlib.util.find_spec(module)
    if spec is None:
        pip_name = _MODULE_TO_PACKAGE.get(module, module.replace(".", "-"))
        return False, f"not installed — pip install {pip_name}"
    try:
        mod = __import__(module)
        version = getattr(mod, "__version__", None) or getattr(mod, "version", None)
        return True, str(version) if version else "installed"
    except Exception:
        return True, "installed (import skipped)"


